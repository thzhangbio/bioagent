export interface BuildSystemPromptOptions {
  companyProfileBlock: string;
  structuredMemoryBlock: string;
  conversationSummaryBlock: string;
}

export function buildSystemPrompt(options: BuildSystemPromptOptions): string {
  return SYSTEM_PROMPT.replace("{company_profile}", options.companyProfileBlock)
    .replace("{structured_memory}", options.structuredMemoryBlock)
    .replace("{conversation_summary}", options.conversationSummaryBlock);
}
