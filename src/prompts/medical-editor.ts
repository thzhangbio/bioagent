export function buildSystemPrompt(companyProfile?: string): string {
  const profile = companyProfile || "尚未了解。如果是首次对话，请主动询问公司基本信息（公司名称、主营产品/服务、目标客户群体、品牌调性）。";
  return SYSTEM_PROMPT.replace("{company_profile}", profile);
}
